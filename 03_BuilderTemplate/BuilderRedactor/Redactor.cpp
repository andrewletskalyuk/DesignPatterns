#include <iostream>
#include <string>
#include <map>
#include <list>

using namespace std;

class Report
{
private:
	string info;
	list<string> instrumentPanel;
public:
	Report(string n) : button(n)
	{
	}
	string getButton(const string& button)
	{
		return instrumentPanel[button];
	}
	void setButton(string button, string value)
	{
		this->instrumentPanel[button] = value;
	}
	void ShowInstrumentsPanel()
	{
		for (auto el : instrumentPanel)
		{
			cout << el.first << " " << el.second << endl;
		}
	}
	bool DoesThisButtonExist(string button)
	{
		return	instrumentPanel.find(button) != instrumentPanel.end() ? true : false;
	}
};

class InstrumentsPanelBuilder
{
protected:
	Report* instrumentpanel;
public:
	virtual void setButton() = 0;
	virtual void addButton() = 0;
	Report* getPanel()
	{
		return this->instrumentpanel;
	}
};

